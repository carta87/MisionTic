    private void crearEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearEnvioActionPerformed

        try {
            String idEnvioo = IdEnvio.getText();
            String idPagoEnv = idPagoEnvio.getText();
            String fecEnvi = fecEnvio.getText();
            String idDirEnvio = dirEnvio.getText();
            String companiaTrans = compañiaTransEnv.getSelectedItem().toString();
            boolean finalizaEnvio = finalizadoEnvio.isSelected();

            if (idPagoEnv.equals("") || idEnvioo.equals("") || fecEnvi.equals("") || idDirEnvio.equals("")) {
                JOptionPane.showMessageDialog(this, "Existen campos en blanco");
            } else {
                boolean flag = false;
                for (Pago pago : pagoList) {

                    if (pago.getIdPago().equals(idPagoEnv)) {
                        Envio envio = new Envio(idEnvioo, idPagoEnv, idDirEnvio, finalizaEnvio, fecEnvi, companiaTrans, pago);
                        envioLista.add(envio);
                        cicloEnvio();
                        JOptionPane.showMessageDialog(this, "Creacion exitosa de envio. valide su envio en la pestaña General");
                        clearCampos();
                        flag = true;
                        break;
                    }

                }
                if (!flag) {

                    JOptionPane.showMessageDialog(this, "No esta registrado el pago con el Id  " + idPagoEnv);
                    clearCampos();
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "falta algun campo ");
        }
    }//GEN-LAST:event_crearEnvioActionPerformed

    public void cicloEnvio() {
        DefaultListModel model = new DefaultListModel();
        for (Envio envio : envioLista) {
            int index = 0;
            String mensajeFinalizado;
            if (envio.getFinalizado() == false) {
                mensajeFinalizado = " no esta finalizado";
            } else {
                mensajeFinalizado = " esta finalizado";
            }
            String data = "ID " + envio.getIdEnvio() + " se enviara por la transportadora " + envio.getCompania() + " y su estado es " + mensajeFinalizado;
            model.add(index, data);
            index++;
        }
        envioList.setModel(model);

    }
