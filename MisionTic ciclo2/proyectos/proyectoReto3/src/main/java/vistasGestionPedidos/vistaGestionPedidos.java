    private void crearPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearPedidoActionPerformed

        try {
            String idPedi = idPedido.getText();
            String fechRealizacion = fecRealizaPed.getText();
            String nomProdValorUni = nomProValor.getSelectedItem().toString();
            boolean estado = estadoPedido.isSelected();
            int cantidad = Integer.parseInt(canProdPedido.getText());
            int total = 0;

            if (nomProdValorUni.equals("* Arroz  Valor Uni $ 1.800")) {
                total = cantidad * 1800;
            }
            if (nomProdValorUni.equals("* Pasta  Valor Uni $ 2.200")) {
                total = cantidad * 2200;
            }
            if (nomProdValorUni.equals("* Panela Valor Uni $ 1.300")) {
                total = cantidad * 1300;
            }

            if (idPedi.equals("") || fechRealizacion.equals("")) {
                JOptionPane.showMessageDialog(this, "Existen Datos Vacios");
            } else {
                Pedido pedido = new Pedido(idPedi, fechRealizacion, estado, total, nomProdValorUni, cantidad);
                pedidoList.add(pedido);
                cicloLista();
                JOptionPane.showMessageDialog(this, "Creacion exitosa");
                clearCampos();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Falta cantidad de los productos");
        }

    }//GEN-LAST:event_crearPedidoActionPerformed

